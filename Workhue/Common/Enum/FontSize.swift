import Foundation

enum FontSize {
    /// 캡션, 보조 텍스트
    static let xs: CGFloat   = 11
    /// 서브텍스트
    static let sm: CGFloat   = 13
    /// 본문
    static let md: CGFloat   = 15
    /// 헤더, 강조 (애플 표준)
    static let lg: CGFloat   = 17
    /// 섹션 타이틀
    static let xl: CGFloat   = 20
    /// 대형 타이틀
    static let xxl: CGFloat  = 24
}
